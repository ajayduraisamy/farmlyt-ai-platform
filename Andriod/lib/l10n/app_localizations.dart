class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  // ─── Cache: langCode → translated bundle ────────────────────────────────
  // Using a static map that PERSISTS across delegate instances but is keyed
  // by locale so different locales never bleed into each other.
  static final Map<String, Map<String, String>> _dynamicBundleCache =
      <String, Map<String, String>>{};

  // Hardcoded languages — no API call needed for these
  static const Set<String> _hardcodedCodes = {
    'en',
    'ta',
    'hi',
    'kn',
    'te',
    'ml',
    'pa',
    'bn',
    'gu',
    'mr',
  };

  @override
  bool isSupported(Locale locale) => locale.languageCode.trim().isNotEmpty;

  @override
  Future<AppLocalizations> load(Locale locale) async {
    final langCode = locale.languageCode;

    // ── Hardcoded languages ────────────────────────────────────────────────
    // These always use the static _translations map — no API call ever needed.
    // We still return a plain AppLocalizations so dynamicTranslations is empty
    // and t() correctly walks: dynamicTranslations → hardcoded locale → 'en'.
    if (_hardcodedCodes.contains(langCode)) {
      return AppLocalizations(locale);
    }

    // ── English: always plain ──────────────────────────────────────────────
    if (langCode == 'en') {
      return AppLocalizations(locale);
    }

    // ── Dynamic languages (Marathi, Gujarati, Bengali, etc.) ──────────────
    // Check cache first
    final cached = _dynamicBundleCache[langCode];
    if (cached != null && cached.isNotEmpty) {
      return AppLocalizations(locale, dynamicTranslations: cached);
    }

    // Translate ALL English keys via TranslatorUtil
    try {
      final englishEntries =
          AppLocalizations._t['en'] ?? const <String, String>{};
      final keys = englishEntries.keys.toList(growable: false);
      final englishValues =
          keys.map((key) => englishEntries[key] ?? key).toList(growable: false);

      final translatedValues = await TranslatorUtil.translateBatch(
        texts: englishValues,
        langCode: langCode,
      );

      final bundle = <String, String>{};
      for (var i = 0; i < keys.length; i++) {
        bundle[keys[i]] =
            (i < translatedValues.length && translatedValues[i].isNotEmpty)
                ? translatedValues[i]
                : englishValues[i]; // fallback to English value for this key
      }

      _dynamicBundleCache[langCode] = bundle;
      return AppLocalizations(locale, dynamicTranslations: bundle);
    } catch (_) {
      // Network/API failure — show English so the app still works
      return AppLocalizations(locale);
    }
  }

