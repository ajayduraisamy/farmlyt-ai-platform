  // ── CRITICAL FIX ─────────────────────────────────────────────────────────
  // shouldReload must return TRUE when the locale changes so Flutter calls
  // load() again with the new locale. Returning false means once the delegate
  // is created it never reloads — language changes have no effect on screens
  // that are already built.
  @override
  bool shouldReload(_AppLocalizationsDelegate old) => true;
